# Calcula area de un triángulo dado tres puntos
def calcular_area(p1, p2, p3):
    return abs(p1[0] * (p2[1] - p3[1]) +
               p2[0] * (p3[1] - p1[1]) +
               p3[0] * (p1[1] - p2[1])) / 2


def graham_scan(puntos):
    # Ordena los puntos por coordenada
    puntos = sorted(puntos)

    def orientacion(p, q, r):
        return (q[1] - p[1]) * (r[0] - q[0]) - (q[0] - p[0]) * (r[1] - q[1])

    # Construye la parte inferior
    inferior = []
    for i in range(len(puntos)):
        p = puntos[i]
        while len(inferior) >= 2 and orientacion(inferior[-2], inferior[-1], p) <= 0:
            inferior.pop()
        inferior.append(p)

    # Construye la parte superior
    superior = []
    for i in range(len(puntos) - 1, -1, -1):
        p = puntos[i]
        while len(superior) >= 2 and orientacion(superior[-2], superior[-1], p) <= 0:
            superior.pop()
        superior.append(p)

    # Elimina el ultimo punto de cada parte porque se repite
    return inferior[:-1] + superior[:-1]


# Encuentra el triangulo con el area máxima
def encontrar_triangulo_max_area(coordenadas):
    envolvente = graham_scan(coordenadas)

    max_area = 0
    triangulo_max = None
    n = len(envolvente)

    for i in range(n):
        for j in range(i + 1, n):
            for k in range(j + 1, n):
                p1 = envolvente[i]
                p2 = envolvente[j]
                p3 = envolvente[k]
                area = calcular_area(p1, p2, p3)
                if area > max_area:
                    max_area = area
                    triangulo_max = (p1, p2, p3)

    return triangulo_max, max_area


